
% glass-epoxy
% given engineering constants

e11=53.48e9;
e22=17.7e9;
ni12=0.278;
ni23=0.4; 
g12=5.83e9;

e33=e22;

ni13=ni12;
ni21=e22/e11*ni12;
ni31=e33/e11*ni13;

ni32=e33/e22*ni23;

g23=e22./2./(1+ni23);
g13=g12;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Sierakowski page 46 eq. 2.33
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
Delta=(1-ni12.*ni21-ni13.*ni31-ni12.*ni23.*ni31-ni13.*ni21.*ni32-ni23.*ni32);
q11=e11.*(1-ni23.*ni32)./Delta;
q22=e22.*(1-ni31.*ni13)./Delta;
q33=e33.*(1-ni12.*ni21)./Delta;
q44=g23;
q55=g13;
q66=g12;
q12=(ni21+ ni31.*ni23).*e11./Delta;
q13=(ni31+ ni21.*ni32).*e11./Delta;
q23=(ni32+ ni12.*ni31).*e22./Delta;

C=[q11, q12, q13, 0 , 0 , 0;
   q12, q22, q23, 0,  0 , 0;
   q13, q23, q33, 0,  0 , 0;
   0,    0,   0,  q44, 0,  0;
   0,    0,   0,   0,  q55, 0;
   0,    0,   0,   0,   0,  q66];

% C =
% 
%    1.0e+10 *
% 
%     5.8465    0.8965    0.8965         0         0         0
%     0.8965    2.2446    0.9803         0         0         0
%     0.8965    0.9803    2.2446         0         0         0
%          0         0         0    0.6321         0         0
%          0         0         0         0    0.5830         0
%          0         0         0         0         0    0.5830

% for 2D case (plate elements)
[q11p,q12p,q22p,q44p,q55p,q66p]=C3D_2_C2D(C(1,1),C(1,2),C(1,3),C(2,2),C(2,3),C(3,3),C(4,4),C(5,5),C(6,6));
